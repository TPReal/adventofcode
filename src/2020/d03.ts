import {log} from "../logger";

export function run() {
  const map = INPUT.split("\n").filter(Boolean);
  const slopes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2],
  ];
  let res = 1;
  for (const [dx, dy] of slopes) {
    let x = 0;
    let y = 0;
    let trees = 0;
    while (y < map.length) {
      if (map[y][x] === "#")
        trees++;
      x += dx;
      x %= map[0].length;
      y += dy;
    }
    res *= trees;
  }
  log(res);
}

